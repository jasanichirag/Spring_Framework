package com.autowiring;

public class Emp {

	private int eid;
	private String name;
	private Add add;
	
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Add getAdd() {
		return add;
	}
	public void setAdd(Add add) {
		this.add = add;
	}
}
